///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#pragma once

#include <windows.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Rect: public tagRECT
{

public:
	Rect(long _left, long _top, long _right, long _bottom)
	{
		left	= _left;
		top		= _top;
		right	= _right;
		bottom	= _bottom;
	}

	Rect(LPRECT pRect)
	{
		left	= pRect->left;
		top		= pRect->top;
		right	= pRect->right;
		bottom	= pRect->bottom;
	}
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#define MAKERECT( l, t, r, b )	( (LPRECT) &ZRect(l, t, r, b) )

