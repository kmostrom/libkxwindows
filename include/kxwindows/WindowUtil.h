
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#pragma once

#include <windows.h>

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class WindowUtil
{
public:
	template<class T>
	T WinGetLong(HWND hWnd, int iWhich)
	{
		#pragma warning(disable:4312)
		return( reinterpret_cast<T> (::GetWindowLong(hWnd, iWhich)) );
		#pragma warning(default:4312)
	}

	template<class T>
	void WinSetLong(HWND hWnd, T tValue, int iWhich)
	{
		#pragma warning(disable:4311)
		::SetWindowLong(hWnd, iWhich, reinterpret_cast<long> (tValue) );
		#pragma warning(default:4311)
	}

};

