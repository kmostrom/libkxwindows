///////////////////////////////////////////////////////////////////////////////////////////////////////

HWND UIListView::Create(HWND parentWindowHandle, UINT id)
{
	mWindowHandle = CreateWindowEx(
		0,
		WC_LISTVIEW,
		"",
		WS_VISIBLE | WS_CHILD,
		0, 0, 220, 127,
		parentWindowHandle,
		(HMENU) id,
		GetModuleHandle(NULL),
		NULL);

	return mWindowHandle;
}

