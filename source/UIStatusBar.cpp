
////////////////////////////////////////////////////////////////////////////////

#include <kxWindows/UIStatusBar.h>

////////////////////////////////////////////////////////////////////////////////

LRESULT UIStatusBar::GetBorders(LPINT borders)
{
	return ::SendMessage(mWindowHandle, SB_GETBORDERS, 0, (LPARAM) borders);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UIStatusBar::GetIcon(int part)
{
	return ::SendMessage(mWindowHandle, SB_GETICON, (WPARAM) part, (LPARAM) 0);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UIStatusBar::GetParts(int partCount, LPINT parts)
{
	return ::SendMessage(mWindowHandle, SB_GETPARTS, (WPARAM) partCount, (LPARAM) parts);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UIStatusBar::GetRect(INT part, LPRECT rect)
{
	return ::SendMessage(mWindowHandle, SB_GETRECT, (WPARAM) part, (LPARAM) rect);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UIStatusBar::GetText(int part, LPSTR text)
{
	return ::SendMessage(mWindowHandle, SB_GETTEXT, (WPARAM) part, (LPARAM) text);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UIStatusBar::GetTextLength(int part)
{
	return ::SendMessage(mWindowHandle, SB_GETTEXTLENGTH, (WPARAM) part, (LPARAM) 0);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UIStatusBar::GetTipText(int part, int size, LPCTSTR toolTip)
{
	return ::SendMessage(mWindowHandle, SB_GETTIPTEXT, MAKEWPARAM(part, size), (LPARAM) toolTip);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UIStatusBar::GetUnicodeFormat(void)
{
	return ::SendMessage(mWindowHandle, SB_GETUNICODEFORMAT, (WPARAM) 0, (LPARAM) 0);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UIStatusBar::IsSimple(void)
{
	return ::SendMessage(mWindowHandle, SB_ISSIMPLE, (WPARAM) 0, (LPARAM) 0);
}

