
////////////////////////////////////////////////////////////////////////////////

#include <kxWindows/UIScrollBar.h>

////////////////////////////////////////////////////////////////////////////////

BOOL UIScrollBar::SetScrollRange(int bar, int minPosition, int maxPosition, BOOL redraw)
{
	return ::SetScrollRange(mWindowHandle, bar, minPosition, maxPosition, redraw);
}

////////////////////////////////////////////////////////////////////////////////

int UIScrollBar::SetScrollPos(int bar, int position, BOOL redraw)
{
	return ::SetScrollPos(mWindowHandle, bar, position, redraw);
}

////////////////////////////////////////////////////////////////////////////////

int UIScrollBar::SetScrollInfo(int bar, LPCSCROLLINFO scrollInfo, BOOL redraw)
{
	return ::SetScrollInfo(mWindowHandle, bar, scrollInfo, redraw);
}

