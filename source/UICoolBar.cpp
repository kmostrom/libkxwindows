///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include <kxWindows/UICoolBar.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

UICoolBar::UICoolBar(HWND parentWindowHandle) // TODO: Add Styles, ExStyle, Width, Size, Vertical etc or add this to ZReBar?
	: UIReBar(parentWindowHandle)
{
	mStyle =	WS_VISIBLE			| WS_BORDER			| WS_CHILD |
				WS_CLIPCHILDREN		| WS_CLIPSIBLINGS	| CCS_NODIVIDER | 
				CCS_NOPARENTALIGN	| RBS_VARHEIGHT		| RBS_BANDBORDERS |
				RBS_AUTOSIZE		| CCS_ADJUSTABLE	| RBS_DBLCLKTOGGLE;

	::GetClientRect(mParentWindowHandle, &mClientRect);

	mClientRect.left = mClientRect.top = 0;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

UICoolBar::~UICoolBar(void) { }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

HWND UICoolBar::CreateToolBarFrame(int x, int y, int width, int height)
{
	POINT point = { 10, 10 };
	::ClientToScreen(mParentWindowHandle, &point);

	y -= point.y;
	x -= point.x;

	return ::CreateWindowEx(WS_EX_TOOLWINDOW, "#32768", NULL, NULL, x, y, width, height, mParentWindowHandle, NULL, ::GetModuleHandle(NULL), NULL);
}

