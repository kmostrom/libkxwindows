
////////////////////////////////////////////////////////////////////////////////

#include <kxWindows/UISysLink.h>

////////////////////////////////////////////////////////////////////////////////

LRESULT UISysLink::GetIdealHeight(void)
{
	return ::SendMessage(mWindowHandle, LM_GETIDEALHEIGHT, (WPARAM) 0, (LPARAM) 0);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UISysLink::GetItem(PLITEM item)
{
	return ::SendMessage(mWindowHandle, LM_GETITEM, (WPARAM) 0, (LPARAM) item);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UISysLink::HitTest(PLHITTESTINFO hitTestInfo)
{
	return ::SendMessage(mWindowHandle, LM_HITTEST, (WPARAM) 0, (LPARAM) hitTestInfo);
}

