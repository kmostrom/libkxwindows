LRESULT UIToolBar::AddCommCtlBitmap(void)
{
	TBADDBITMAP tbab;
	tbab.hInst	= HINST_COMMCTRL;
	tbab.nID	= IDB_STD_SMALL_COLOR;
	return ::SendMessage(mWindowHandle, TB_ADDBITMAP, 0, (WPARAM) &tbab);
}

//////////////////////////////////////////////////////////////////////////////////////

BOOL UIToolBar::AddButton(int iBitmap, int iCommand, char cState, char cStyle, DWORD_PTR dwData, INT_PTR piString)
{
	TBBUTTON tb;
	tb.iBitmap		= iBitmap;
	tb.idCommand	= iCommand;
	tb.fsState		= cState;
	tb.fsStyle		= cStyle;
	tb.dwData		= dwData;
	tb.iString		= piString;
	return (BOOL) AddButtons(1, &tb);
}

