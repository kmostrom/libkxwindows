
////////////////////////////////////////////////////////////////////////////////

#include <kxWindows/UIHotKey.h>

////////////////////////////////////////////////////////////////////////////////

LRESULT UIHotKey::GetHotKey(void)
{
	return ::SendMessage(mWindowHandle, HKM_GETHOTKEY, (WPARAM) 0, (LPARAM) 0);
}

////////////////////////////////////////////////////////////////////////////////

LRESULT UIHotKey::SetHotKey(UINT virtualKey, UINT keyModifier)
{
	return ::SendMessage(mWindowHandle, HKM_SETHOTKEY, (WPARAM) MAKEWPARAM(virtualKey, keyModifier), (LPARAM) 0);
}

////////////////////////////////////////////////////////////////////////////////

LRESULT UIHotKey::SetRules(DWORD keyCombinations, DWORD invalidModifiers)
{
	return ::SendMessage(mWindowHandle, HKM_SETRULES, (WPARAM) keyCombinations, MAKELPARAM(invalidModifiers, 0));
}

