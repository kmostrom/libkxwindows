// CPP ////////////////////////////////////////////////////////////////////////////////////////////////////

#include <kxWindows/UIAnimate.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UIAnimate::Open(HINSTANCE instanceHandle, LPSTR name)
{
	return ::SendMessage(mWindowHandle, ACM_OPEN, (WPARAM) instanceHandle, (LPARAM) name);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UIAnimate::Play(UINT repeat, WORD from, WORD to)
{
	return ::SendMessage(mWindowHandle, ACM_PLAY, (WPARAM) repeat, (LPARAM) MAKELONG(from, to));
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UIAnimate::Stop(void)
{
	return ::SendMessage(mWindowHandle, ACM_STOP, (WPARAM) 0, (LPARAM) 0);
}

