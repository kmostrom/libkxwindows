
////////////////////////////////////////////////////////////////////////////////

#include <kxWindows/UIDateTimePicker.h>

////////////////////////////////////////////////////////////////////////////////

LRESULT UIDateTimePicker::GetMCColor(int color)
{
	return ::SendMessage(mWindowHandle, DTM_GETMCCOLOR, (WPARAM) color, (LPARAM) 0);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UIDateTimePicker::GetMCFont(void)
{
	return ::SendMessage(mWindowHandle, DTM_GETMCFONT, (WPARAM) 0, (LPARAM) 0);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UIDateTimePicker::GetMonthCal(void)
{
	return ::SendMessage(mWindowHandle, DTM_GETMONTHCAL, (WPARAM) 0, (LPARAM) 0);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UIDateTimePicker::GetRange(LPSYSTEMTIME systemTime)
{
	return ::SendMessage(mWindowHandle, DTM_GETRANGE, (WPARAM) 0, (LPARAM) systemTime);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UIDateTimePicker::GetSystemTime(LPSYSTEMTIME systemTime)
{
	return ::SendMessage(mWindowHandle, DTM_GETSYSTEMTIME, (WPARAM) 0, (LPARAM) systemTime);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UIDateTimePicker::SetFormat(LPCTSTR format)
{
	return ::SendMessage(mWindowHandle, DTM_SETFORMAT, (WPARAM) 0, (LPARAM) format);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UIDateTimePicker::SetMCColor(int color, COLORREF colorStruct)
{
	return ::SendMessage(mWindowHandle, DTM_SETMCCOLOR, (WPARAM) color, (LPARAM) colorStruct);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UIDateTimePicker::SetMCFont(HFONT font, BOOL redraw)
{
	return ::SendMessage(mWindowHandle, DTM_SETMCFONT, (WPARAM) font, (LPARAM) MAKELONG(redraw, 0));
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UIDateTimePicker::SetRange(DWORD flags, LPSYSTEMTIME systemTime)
{
	return ::SendMessage(mWindowHandle, DTM_SETRANGE, (WPARAM) flags, (LPARAM) systemTime);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UIDateTimePicker::SetSystemTime(DWORD flags, LPSYSTEMTIME systemTime)
{
	return ::SendMessage(mWindowHandle, DTM_SETSYSTEMTIME, (WPARAM) flags, (LPARAM) systemTime);
}

