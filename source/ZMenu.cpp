/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include <kxWindows/ZMenu.h>

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

ZMenu::ZMenu(void)
{
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

ZMenu::~ZMenu(void)
{
	::DestroyMenu( _hMenu );
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

HMENU ZMenu::LoadMenu(UINT uiMenuID)
{
	_hMenu = ::LoadMenu( ::GetModuleHandle(NULL), MAKEINTRESOURCE(uiMenuID) );
	return( _hMenu );
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool ZMenu::Enable(UINT uiMenuItemID, UINT uiEnable)
{
	return( ::EnableMenuItem( _hMenu, uiMenuItemID, uiEnable ) != -1 );
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

HMENU ZMenu::GetMenuHandle(void)
{
	return( _hMenu );
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void ZMenu::SetMenu(HWND hWndParent)
{
	::SetMenu( hWndParent, _hMenu );
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

HMENU ZMenu::GetSubMenu(int iPos)
{
	return( ::GetSubMenu( _hMenu, iPos) );
}

