
////////////////////////////////////////////////////////////////////////////////

#include <kxWindows/UIIPAddress.h>

////////////////////////////////////////////////////////////////////////////////

LRESULT UIIPAddress::ClearAddress(void)
{
	return ::SendMessage(mWindowHandle, IPM_CLEARADDRESS, (WPARAM) 0, (LPARAM) 0);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////

LRESULT UIIPAddress::GetAddress(LPDWORD address)
{
	return ::SendMessage(mWindowHandle, IPM_GETADDRESS, (WPARAM) 0, (LPARAM) address);
}

